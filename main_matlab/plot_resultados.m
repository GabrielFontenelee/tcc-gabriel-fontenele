function configureTimeAxis(ax, time_start, time_end, plot_title, y_label)
    grid(ax, 'on');
    box(ax, 'on');
    xlim(ax, [time_start, time_end]);

    % Align the x-axis markers with the beginning of each 10-second cycle.
    cycle_duration = 10;
    first_cycle_tick = ceil(time_start / cycle_duration) * cycle_duration;
    last_cycle_tick = floor(time_end / cycle_duration) * cycle_duration;

    if first_cycle_tick <= last_cycle_tick
        xticks(ax, first_cycle_tick:cycle_duration:last_cycle_tick);
    end

    title( ...
        ax, ...
        plot_title, ...
        'FontName', 'Times New Roman', ...
        'FontSize', 14, ...
        'FontWeight', 'bold', ...
        'Interpreter', 'tex');

    xlabel( ...
        ax, ...
        'Time (s)', ...
        'FontName', 'Times New Roman', ...
        'FontSize', 13, ...
        'FontWeight', 'bold');

    ylabel( ...
        ax, ...
        y_label, ...
        'FontName', 'Times New Roman', ...
        'FontSize', 13, ...
        'FontWeight', 'bold', ...
        'Interpreter', 'tex');

    ax.FontName = 'Times New Roman';
    ax.FontSize = 12;
    ax.FontWeight = 'bold';
    ax.LineWidth = 1.0;
end

