function values = getMetricColumn(metric_data, joint)
    values = squeeze(metric_data);

    if ~ismatrix(values)
        error('Metric data must be a two-dimensional array.');
    end

    if size(values, 1) > 3 && size(values, 2) >= joint
        values = values(:, joint);
    elseif size(values, 1) >= joint
        values = values(joint, :).';
    else
        error('Metric data do not contain joint %d.', joint);
    end

    values = values(:);
    values = values(isfinite(values));
end

function [time, data] = getSingleSignal(signal)
    [time, data] = getMultichannelSignal(signal);

    if size(data, 2) > 1
        data = data(:, 1);
    end

    data = data(:);
end

function [time, data] = getMultichannelSignal(signal)
    if isa(signal, 'timeseries')
        time = signal.Time;
        data = signal.Data;
    elseif isstruct(signal) && ...
            isfield(signal, 'Time') && ...
            isfield(signal, 'Data')
        time = signal.Time;
        data = signal.Data;
    else
        error( ...
            ['Signal data must be a timeseries or a structure ', ...
             'containing Time and Data fields.']);
    end

    time = time(:);
    data = squeeze(data);

    if isvector(data)
        data = data(:);
    end

    if size(data, 1) ~= numel(time) && size(data, 2) == numel(time)
        data = data.';
    end

    number_of_samples = min(numel(time), size(data, 1));
    time = time(1:number_of_samples);
    data = data(1:number_of_samples, :);

    valid_samples = isfinite(time) & all(isfinite(data), 2);
    time = time(valid_samples);
    data = data(valid_samples, :);

    [time, unique_indices] = unique(time, 'stable');
    data = data(unique_indices, :);

    if isempty(time)
        error('Signal data contain no valid samples.');
    end
end

function [time_plot, signal_plot] = cropAndReduceSignal( ...
    time, ...
    signal, ...
    time_start, ...
    time_end, ...
    max_points)

    mask = time >= time_start & time <= time_end;
    time_plot = time(mask);
    signal_plot = signal(mask);

    if isempty(time_plot)
        return;
    end

    number_of_points = numel(time_plot);
    if number_of_points > max_points
        indices = unique(round(linspace(1, number_of_points, max_points)));
        time_plot = time_plot(indices);
        signal_plot = signal_plot(indices);
    end
end
