function saveInitialConditionFigure( ...
    data, ...
    output_dir, ...
    visibility, ...
    time_start, ...
    requested_time_end, ...
    max_points, ...
    joint_names, ...
    scenario_name, ...
    colors)

    if ~isfield(data, 'error_initialCondition')
        warning('The variable error_initialCondition was not found.');
        return;
    end

    [time, initial_error] = getMultichannelSignal( ...
        data.error_initialCondition);

    if size(initial_error, 2) < 5
        warning( ...
            ['The variable error_initialCondition must contain the ', ...
             'three angular-error channels.']);
        return;
    end

    time_start_plot = max(time_start, time(1));
    time_end = min(requested_time_end, time(end));
    if time_end <= time_start_plot
        warning( ...
            'No initial-condition-error data are available in the requested interval.');
        return;
    end

    angular_channels = [1 3 5];

    fig = createFigure( ...
        sprintf('%s - Initial-Condition Errors', scenario_name), ...
        visibility, ...
        [60 60 1450 980]);

    layout = tiledlayout( ...
        fig, ...
        3, ...
        1, ...
        'TileSpacing', 'compact', ...
        'Padding', 'compact');

    for joint = 1:3
        ax = nexttile(layout);

        [time_plot, error_plot] = cropAndReduceSignal( ...
            time, ...
            initial_error(:, angular_channels(joint)), ...
            time_start_plot, ...
            time_end, ...
            max_points);

        plot( ...
            ax, ...
            time_plot, ...
            error_plot, ...
            '-', ...
            'Color', colors.perturbation, ...
            'LineWidth', 1.7);

        configureTimeAxis( ...
            ax, ...
            time_start_plot, ...
            time_end, ...
            sprintf('Joint %s Initial-Condition Error over Time', ...
                joint_names{joint}), ...
            'Injected error (deg)');
    end

    title( ...
        layout, ...
        sprintf('%s - Injected Initial-Condition Errors over Time', ...
            scenario_name), ...
        'FontName', 'Times New Roman', ...
        'FontSize', 17, ...
        'FontWeight', 'bold', ...
        'Interpreter', 'tex');

    applyFigureFont(fig);
    exportFigure( ...
        fig, ...
        fullfile(output_dir, 'Scenario_05_Initial_Condition_Error.png'), ...
        visibility);
end

