                output_dir, ...
                visibility, ...
                time_start, ...
                requested_time_end, ...
                max_points, ...
                joint_names, ...
                scenario_name, ...
                colors);

        case 4
            savePayloadFigure( ...
                data, ...
                output_dir, ...
                visibility, ...
                time_start, ...
                requested_time_end, ...
                max_points, ...
                scenario_name, ...
                colors);

        case 5
            saveInitialConditionFigure( ...
                data, ...
                output_dir, ...
                visibility, ...
                time_start, ...
                requested_time_end, ...
                max_points, ...
                joint_names, ...
                scenario_name, ...
                colors);
    end
end

function saveNoiseFigure( ...
    data, ...
    output_dir, ...
    visibility, ...
    time_start, ...
    requested_time_end, ...
    max_points, ...
    joint_names, ...
    scenario_name, ...
    colors)

    if ~isfield(data, 'noises')
        warning('The variable noises was not found.');
        return;
    end

    [time, noise] = getMultichannelSignal(data.noises);
    if size(noise, 2) < 6
        warning('The variable noises must contain six channels.');
        return;
    end

    time_start_plot = max(time_start, time(1));
    time_end = min(requested_time_end, time(end));
    if time_end <= time_start_plot
        warning('No noise data are available in the requested interval.');
        return;
    end

    fig = createFigure( ...
        sprintf('%s - Injected Noise', scenario_name), ...
        visibility, ...
        [50 50 1550 1000]);

    layout = tiledlayout( ...
        fig, ...
        3, ...
        2, ...
        'TileSpacing', 'compact', ...
        'Padding', 'compact');

    for joint = 1:3
        for signal_type = 1:2
            channel = 2 * (joint - 1) + signal_type;
            ax = nexttile(layout);

            [time_plot, noise_plot] = cropAndReduceSignal( ...
                time, ...
                noise(:, channel), ...
                time_start_plot, ...
                time_end, ...
                max_points);

            plot( ...
                ax, ...
                time_plot, ...
                noise_plot, ...
                '-', ...
                'Color', colors.perturbation, ...
                'LineWidth', 1.25);

            if signal_type == 1
                signal_title = 'Position Noise over Time';
                y_label = 'Noise (deg)';
            else
                signal_title = 'Angular-Velocity Noise over Time';
                y_label = 'Noise (rad/s)';
            end

            configureTimeAxis( ...
                ax, ...
                time_start_plot, ...
                time_end, ...
                sprintf('Joint %s - %s', ...
                    joint_names{joint}, signal_title), ...
                y_label);
        end
    end

    title( ...
        layout, ...
        sprintf('%s - Injected Noise over Time', scenario_name), ...
        'FontName', 'Times New Roman', ...
        'FontSize', 17, ...
        'FontWeight', 'bold', ...
        'Interpreter', 'tex');

    applyFigureFont(fig);
    exportFigure( ...
        fig, ...
        fullfile(output_dir, 'Scenario_02_Injected_Noise.png'), ...
        visibility);
end

