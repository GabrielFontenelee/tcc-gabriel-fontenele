    data, ...
    scenario, ...
    output_dir, ...
    visibility, ...
    time_start, ...
    requested_time_end, ...
    max_points, ...
    joint_names, ...
    scenario_name, ...
    colors)

    switch scenario
        case 2
            saveNoiseFigure( ...
                data, ...
